using System;

namespace _09.Reversed_chars
{
    class Program
    {
        static void Main(string[] args)
        {
            char a = char.Parse(Console.ReadLine());
            char b = char.Parse(Console.ReadLine());
            char c = char.Parse(Console.ReadLine());
            Console.Write(c);
            Console.Write(b);
            Console.WriteLine(a);
        }
    }
}
